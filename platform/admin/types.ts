/**
 * platform/admin/types.ts — Config management types
 *
 * Types for the config management agent, tool handlers, two-person
 * approval workflow, change history, impact correlation, and validation.
 *
 * Maps to Migration 011 schema:
 *   - Enhanced platform_config (value_type, permission_tier, validation metadata)
 *   - platform_config_history (full change audit trail)
 *   - config_pending_approvals (two-person approval, built disabled)
 *
 * GenAI Principles:
 *   P2  — Agentic execution: config agent is bounded, multi-step, instrumented
 *   P3  — Total observability: every config change traced with trajectory
 *   P5  — Versioned artifacts: tool definitions typed and registerable
 *   P6  — Structured outputs: all agent results enforce schemas
 *   P10 — Human oversight: reconfirmation + optional two-person approval
 *   P12 — Economic transparency: agent cost tracked per trajectory
 *   P13 — Control plane: permission tiers enforce admin vs super_admin
 *   P15 — Agent identity: config agent has actorType/actorId/agentRole
 *   P17 — Cognition-commitment: evaluate internally, commit once (with confirm)
 *   P18 — Durable trajectories: full step history per conversation turn
 *
 * @module platform/admin
 */

import type { Step } from "@/platform/agents/types";

// ---------------------------------------------------------------------------
// Config value types — drives UI widget + validation (Migration 011)
// ---------------------------------------------------------------------------

/**
 * Value type for a config entry. Determines the UI input widget
 * and the validation rules applied on update.
 *
 *   string      — free-form text
 *   number      — numeric, respects min_value/max_value
 *   boolean     — true/false toggle
 *   string_enum — constrained to allowed_values list
 *   json_array  — JSON array (e.g., supported_languages)
 */
export type ConfigValueType =
  | "string"
  | "number"
  | "boolean"
  | "string_enum"
  | "json_array";

// ---------------------------------------------------------------------------
// Permission tiers — who can change what (Migration 011)
// ---------------------------------------------------------------------------

/**
 * Permission tier for a config entry.
 *
 *   standard — admin or super_admin can change (config_manage_standard)
 *   safety   — super_admin only (config_manage_safety). Covers moderation
 *              thresholds, strike limits, COPPA settings, system flags.
 */
export type PermissionTier = "standard" | "safety";

// ---------------------------------------------------------------------------
// Enhanced config entry — includes Migration 011 metadata
// ---------------------------------------------------------------------------

/**
 * Full config entry with validation metadata from Migration 011.
 * Extends the original ConfigEntry with default_value, value_type,
 * min/max constraints, allowed values, and permission tier.
 */
export interface EnhancedConfigEntry {
  readonly key: string;
  readonly value: unknown;
  readonly description: string | null;
  readonly category: string;
  readonly updatedAt: string;
  /** Intended default value from seed migration */
  readonly defaultValue: unknown;
  /** Value type — drives validation and UI widget */
  readonly valueType: ConfigValueType;
  /** Minimum allowed value (for number type) */
  readonly minValue: number | null;
  /** Maximum allowed value (for number type) */
  readonly maxValue: number | null;
  /** Allowed values (for string_enum type) */
  readonly allowedValues: readonly string[] | null;
  /** Permission tier: standard (admin) or safety (super_admin) */
  readonly permissionTier: PermissionTier;
}

// ---------------------------------------------------------------------------
// Config validation
// ---------------------------------------------------------------------------

/** Result of validating a proposed config value */
export interface ConfigValidationResult {
  readonly valid: boolean;
  readonly errors: readonly string[];
}

// ---------------------------------------------------------------------------
// Config change request — what the agent produces before confirmation
// ---------------------------------------------------------------------------

/**
 * A proposed config change. Created by the config agent's update tool
 * before the human confirms. Contains all information needed for the
 * reconfirmation flow (P10).
 */
export interface ConfigChangeRequest {
  /** Config key being changed */
  readonly key: string;
  /** Current value (for display and rollback) */
  readonly currentValue: unknown;
  /** Proposed new value */
  readonly proposedValue: unknown;
  /** Why the change matters — generated by the agent */
  readonly impact: string;
  /** Who is affected — generated by the agent */
  readonly affectedUsers: string;
  /** Whether this is reversible */
  readonly reversible: boolean;
  /** Permission tier of this config entry */
  readonly permissionTier: PermissionTier;
  /** Whether two-person approval is required for this change */
  readonly requiresApproval: boolean;
  /** Mandatory change comment from the admin */
  readonly changeComment: string;
}

// ---------------------------------------------------------------------------
// Config history — maps to platform_config_history table
// ---------------------------------------------------------------------------

/**
 * Source of a config change. Tracks how the change was made.
 */
export type ConfigChangeSource =
  | "admin_ui"
  | "config_agent"
  | "api"
  | "migration"
  | "system";

/**
 * A single config change history record.
 * Maps 1:1 to the platform_config_history table.
 */
export interface ConfigHistoryRecord {
  readonly id: string;
  readonly configKey: string;
  readonly previousValue: unknown;
  readonly newValue: unknown;
  readonly changedBy: string | null;
  readonly changeComment: string;
  readonly changeSource: ConfigChangeSource;
  readonly createdAt: string;
}

/** Options for querying config history */
export interface ConfigHistoryOptions {
  readonly configKey?: string;
  readonly changedBy?: string;
  readonly since?: string;
  readonly before?: string;
  readonly limit?: number;
}

