// ---------------------------------------------------------------------------
// Gotchas (L17) — module-level scar tissue
// ---------------------------------------------------------------------------
//
// 1. EnhancedConfigEntry.value and defaultValue are `unknown` — the DB
//    stores JSONB. Always validate with valueType before casting.
//    A "number" config stores its value as a JSON number, but Supabase
//    may return it as a string. Parse based on valueType.
//
// 2. ConfigApprovalRecord.requestedBy and reviewedBy are UUID strings,
//    not user objects. Join with users table if display name is needed.
//
// 3. PermissionTier is "standard" | "safety" — the DB column is TEXT,
//    not an enum. Validate at the boundary (API route or service layer).
//
// 4. ConfigHistoryRecord.previousValue can be null for the first change
//    to a key (no previous value existed).
//
// 5. ConfigChangeSource must match the DB default 'admin_ui'. The config
//    agent should always set change_source = 'config_agent'.
//
// 6. minValue/maxValue are stored as JSONB in the DB (not native numbers).
//    Migration 011 uses string literals like '1', '0.0'. Parse to number
//    at the service boundary.
//
// 7. allowedValues is a JSONB column storing a JSON array of strings.
//    Parse with JSON.parse if it arrives as a string from Supabase.
