// ---------------------------------------------------------------------------
// Impact correlation — joins config history with moderation audit
// ---------------------------------------------------------------------------

/**
 * Impact report for a config change. Shows what happened to moderation
 * outcomes after the change was made.
 *
 * Example: "After you changed moderation.level2.block_severity from
 * 'medium' to 'high', the block rate for Level 2 users dropped from
 * 12% to 4%."
 */
export interface ConfigImpactReport {
  /** The config change that was analyzed */
  readonly change: ConfigHistoryRecord;
  /** Period analyzed: from change time to now (or next change) */
  readonly periodStart: string;
  readonly periodEnd: string;
  /** Moderation stats before the change */
  readonly before: ConfigImpactMetrics;
  /** Moderation stats after the change */
  readonly after: ConfigImpactMetrics;
  /** Human-readable summary */
  readonly summary: string;
}

/** Moderation metrics for impact comparison */
export interface ConfigImpactMetrics {
  readonly totalScreenings: number;
  readonly blockCount: number;
  readonly warnCount: number;
  readonly allowCount: number;
  readonly escalateCount: number;
  readonly blockRate: number;
  readonly warnRate: number;
}

