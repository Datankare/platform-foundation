// ---------------------------------------------------------------------------
// Config agent result — what the agent returns per conversation turn
// ---------------------------------------------------------------------------

/**
 * Tool call result from a single config agent tool invocation.
 * The config agent may invoke multiple tools per conversation turn;
 * each produces one of these results.
 */
export interface ConfigToolResult {
  /** Which tool was called */
  readonly toolId: string;
  /** Whether the tool call succeeded */
  readonly success: boolean;
  /** Tool output data */
  readonly data: unknown;
  /** Error message if failed */
  readonly error?: string;
  /** Duration of this tool call in ms */
  readonly durationMs: number;
}

/**
 * Full result from a config agent conversation turn.
 * Contains the agent's response, any tool calls made, and the
 * trajectory for observability.
 */
export interface ConfigAgentResult {
  /** Agent's natural language response to the admin */
  readonly response: string;
  /** Tool calls made during this turn */
  readonly toolResults: readonly ConfigToolResult[];
  /** Whether a config change is pending confirmation */
  readonly pendingChange: ConfigChangeRequest | null;
  /** Full trajectory for this turn (P18) */
  readonly trajectoryId: string;
  readonly agentId: string;
  readonly steps: readonly Step[];
  /** Total cost of this turn in USD */
  readonly totalCost: number;
  /** Total latency of this turn in ms */
  readonly totalLatencyMs: number;
}

